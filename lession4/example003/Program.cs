void GetArray(int[] arr)
{
        int length = arr.Length;
        for(int i = 0; i < length; i++)
        {
                arr[i] = new Random().Next(0, 2);
        }
}

void PrintArray(int[] arr)
{
        int length = arr.Length;
        for(int  i = 0; i < length; i++)
        {
                Console.Write($"{arr[i]},");
        }
}


int[] array = new int[8];

GetArray(array);
PrintArray(array);



